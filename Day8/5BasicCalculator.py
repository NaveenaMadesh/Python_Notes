def add(num1=0,num2=0):
    print(num1+num2)

def sub(num1=0,num2=0):
    print(num1-num2)

def mul(num1=0,num2=0):
    print(num1*num2)

def div(num1=0,num2=0):
    print(num1//num2)

num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))

add(num1,num2)
sub(num1,num2)
mul(num1,num2)
div(num1,num2)
